def the_brutest_of_forces( N) :
    cnt = 4
    for x in range( 1, N//2+1 ):
        for y in range(N//2 , 1, -1) :
            if x != 0 :
                if y/x < 1 : break
            if (x - N/2)**2 + ( y - N/2 )**2 == N**2/4  :
                cnt+=8
                print('points = ',str(cnt)+'.      x =', x ,'     y=', y, '     ', N**2/4 )

the_brutest_of_forces(10**4)


